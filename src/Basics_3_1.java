public class Basics_3_1 {
    public static void main(String[] args) {
        long n = 1_000_000_000; // eine Milliarde
        System.out.println("Eine Milliarden: " + n);
        System.out.println("Zwei Milliarden: " + (n + n));
        System.out.println("Drei Milliarden: " + (n + n + n));
    }
}
